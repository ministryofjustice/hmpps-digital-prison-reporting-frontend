export const generateAgeBreakdownData = (establishment, wing) => {
  let data = [
    ...mockTotalTableData,
    ...mockAgeRange1TableData,
    ...mockAgeRange2TableData,
    ...mockReligionTableData,
    ...mockEthnicTableData,
    ...mockNationalityTableData,
  ]

  if (establishment) {
    data = data.map((d) => {
      return {
        ...d,
        establishment_id: { raw: establishment },
      }
    })
  } else {
    data = data.map((d) => {
      return {
        ...d,
        establishment_id: { raw: 'All' },
      }
    })
  }

  if (wing) {
    data = [...data, ...mockTotalByWingTableData, ...mockCellTableData]
    data = data.map((d) => {
      if (d.wing.raw === '') {
        return d
      }
      return {
        ...d,
        wing: { raw: wing },
      }
    })
  } else {
    data = data.map((d) => {
      return {
        ...d,
        wing: { raw: '' },
      }
    })
  }
  return data
}

